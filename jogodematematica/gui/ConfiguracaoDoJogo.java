    public ArrayList<String> capturaOperacaoMatematica() {

        criaListaBotoesOperacoes();

        if (todasOperacoesSelecionadas()) {
            return operacoes();
        }
        ArrayList<String> operacoes = new ArrayList<String>();
        for (int i = 0; i < 4; i++) {
            if (selectedOperacoes.get(i).isSelected()) {
                if (selectedOperacoes.get(i) == jCBAdicao) {
                    operacoes.add("+");
                }
                if (selectedOperacoes.get(i) == jCBSubtracao) {
                    operacoes.add("-");
                }
                if (selectedOperacoes.get(i) == jCBMultiplicacao) {
                    operacoes.add("*");
                }
                if (selectedOperacoes.get(i) == jCBDivisao) {
                    operacoes.add("/");
                }

            }
        }

        for (String i : operacoes) {
            System.out.println("Operacoes selecionado(s): " + i);
        }
        return operacoes;
    }

    public ArrayList<Integer> capturarValoresParaTabuada() {

        criaListaBotoesTabuada();
        //Captura todos os botoes selecionados.
        //armazena em @tabuada o numero da tabuada que se quer responder
        //

        if (todasTabuadasSelecionadas()) {
            return tabuadas();
        }

        ArrayList<Integer> tabuada = new ArrayList<Integer>();
        for (int i = 0; i < 10; i++) {
            if (selectedButtons.get(i).isSelected()) {
                tabuada.add(Integer.parseInt(selectedButtons.get(i).getText()));
            }
        }
        //teste
        for (int i : tabuada) {
            System.out.println("Valor(es) selecionado(s): " + i);
        }

        return tabuada;

    }

