    public void criaListaBotoesOperacoes() {
        selectedOperacoes.clear();
        selectedOperacoes.add(jCBAdicao);
        selectedOperacoes.add(jCBMultiplicacao);
        selectedOperacoes.add(jCBSubtracao);
        selectedOperacoes.add(jCBDivisao);
    }

    private boolean todasTabuadasSelecionadas() {
        criaListaBotoesTabuada();
        boolean vf = false;
        for (int i = 0; i < 10; i++) {
            if (jCBTabuadaTodas.isSelected()) {
                selectedButtons.get(i).setSelected(false);
                selectedButtons.get(i).setEnabled(false);
                vf = true;
            } else {
                selectedButtons.get(i).setEnabled(true);
                vf = false;
            }
        }
        return vf;
    }

    private boolean todasOperacoesSelecionadas() {
        criaListaBotoesOperacoes();
        boolean vf = false;
        for (int i = 0; i < 4; i++) {
            if (jCBOperacaoTodas.isSelected()) {
                selectedOperacoes.get(i).setSelected(false);
                selectedOperacoes.get(i).setEnabled(false);
                vf = true;
            } else {
                selectedOperacoes.get(i).setEnabled(true);
                vf = false;
            }
        }
        return vf;
    }

    private ArrayList<String> operacoes() {
        ArrayList<String> operacoes = new ArrayList<>();
        operacoes.add("+");
        operacoes.add("-");
        operacoes.add("*");
        operacoes.add("/");

        return operacoes;
    }

    private ArrayList<Integer> tabuadas() {
        ArrayList<Integer> tabuadas = new ArrayList<>();

        for (int i = 1; i < 11; i++) {
            tabuadas.add(i);
        }
        return tabuadas;
    }

